/*
 * text_to_graphics
 *   DESCRIPTION: Show the input string to the logical view window on the video display.
 *   INPUTS: buffer - array holding which pixels should be colored
 *           str - input string to display to status bar
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: displays the current level, fruit count, and timer for a level
 */
void text_to_graphics(unsigned char * buffer, char * str){
    int i, r, bit, idx, p_off;
    int plane_size = 1440; //5760/4 for every plane
    unsigned char mask; //to compare where 1 bit
    //unsigned char* buffer = buffer_input;
    int center = ((IMAGE_X_DIM - (strlen(str) * 8))/2)/4;//finds center offset to begin depending on num of pixels within a row of str
    unsigned char* original = buffer + 80 + center;//start at center of second row by skipping 80 addr spaces

    for(i = 0; i < strlen(str); i++){//for every char
        buffer = original + (i * 2);//address offset for current char block
        for(r = 0; r < 16; r++){//default height of 16
            mask = 0x80;//reset for every row
            for(bit = 0; bit < 8; bit++){//for every col 8 bits per char) //
                p_off = bit % 4; //p_off = 3 - (3&bit);
                idx = p_off * plane_size;//index to buffer
                if (bit == 4){//2 addresses for every row, need to go to next one after 4 pixels
                    buffer++;
                }
                if(font_data[(int)str[i]][r] & mask){//if 1 bit
                    buffer[idx] = 0x1c;//color pixel
                }
               
                mask = mask >> 1;//look at next bit
            
            }
            buffer += 79;//got to start of next row to continue writing char
            
        }
        
    }
    buffer = original;//set pointer back to beginning of array
}

void floating_text(unsigned char * buffer, char * str){
    int i, r, bit, idx, start;
    unsigned char mask;
    start = ((FLOAT_WIDTH - (strlen(str) * 8))/2); //where to start writing to - middle of text block
    for(i = 0; i < strlen(str); i++){
        idx = start + (i*8); //pixels to pass to get to next block
        for(r = 0; r < 16; r++){ //for every row of text
            mask = 0x80;//reset for every row
            for(bit = 0; bit < 8; bit++){//for every bit in char block
                if(font_data[(int)str[i]][r] & mask){//if 1 bit
                    buffer[idx] = 0x32;//color the pixel
                }
                idx++; //look at next pixel
                mask = mask >> 1;//look at next bit
            }
            idx += FLOAT_WIDTH - 8; //go to left side of next row of char block
        }
    }
}
            

